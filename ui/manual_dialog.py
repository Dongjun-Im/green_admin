def _intro_text() -> str:
    return (
        f"{APP_NAME} 사용 설명서  (버전 {APP_VERSION})\n\n"
        "왼쪽 챕터 목록에서 위/아래 화살표로 챕터를 고르면, "
        "이 오른쪽 칸에 그 챕터 내용이 표시됩니다.\n"
        "Tab 키로 챕터 목록과 내용 칸 사이를 오갈 수 있습니다.\n"
        "닫으려면 Esc 키나 닫기 버튼을 누르세요.\n"
    )


class ManualDialog(wx.Dialog):
    """사용 설명서 — 챕터 목록 + 본문. Shift+F1 로 열림."""

    def __init__(self, parent):
        super().__init__(
            parent,
            title=f"{APP_NAME} 사용 설명서",
            style=wx.DEFAULT_DIALOG_STYLE | wx.RESIZE_BORDER | wx.MAXIMIZE_BOX,
        )
        panel = wx.Panel(self)
        outer = wx.BoxSizer(wx.VERTICAL)

        # 좌우 분할: 왼쪽 챕터 목록 | 오른쪽 본문
        body = wx.BoxSizer(wx.HORIZONTAL)

        # ----- 왼쪽: 챕터 목록 -----
        left = wx.BoxSizer(wx.VERTICAL)
        left.Add(
            wx.StaticText(panel, label=f"챕터 목록 - 모두 {len(CHAPTERS)}개(&L):"),
            0, wx.LEFT | wx.RIGHT | wx.TOP, 8,
        )
        self.chapter_list = wx.ListBox(
            panel,
            choices=[f"{i + 1}. {title}" for i, (title, _) in enumerate(CHAPTERS)],
            name="챕터 목록",
        )
        self.chapter_list.SetMinSize(wx.Size(280, -1))
        self.chapter_list.Bind(wx.EVT_LISTBOX, self._on_chapter_changed)
        self.chapter_list.Bind(wx.EVT_LISTBOX_DCLICK, self._on_chapter_dclick)
        left.Add(self.chapter_list, 1, wx.EXPAND | wx.ALL, 8)

        body.Add(left, 0, wx.EXPAND)

        # ----- 오른쪽: 본문 -----
        right = wx.BoxSizer(wx.VERTICAL)
        self.body_label = wx.StaticText(panel, label="내용(&M):")
        right.Add(self.body_label, 0, wx.LEFT | wx.RIGHT | wx.TOP, 8)
        self.text = wx.TextCtrl(
            panel,
            value=_intro_text(),
            style=wx.TE_READONLY | wx.TE_MULTILINE | wx.TE_DONTWRAP,
            name="사용 설명서 내용",
        )
        right.Add(self.text, 1, wx.EXPAND | wx.ALL, 8)

        body.Add(right, 1, wx.EXPAND)
        outer.Add(body, 1, wx.EXPAND)

        # ----- 닫기 버튼 -----
        close_btn = wx.Button(panel, wx.ID_OK, "닫기(&C)")
        close_btn.SetDefault()
        outer.Add(close_btn, 0, wx.ALIGN_CENTER | wx.ALL, 10)

        panel.SetSizer(outer)
        self.SetEscapeId(wx.ID_OK)
        self.SetMinSize(wx.Size(880, 600))
        self.Fit()
        self.Centre()

        # 처음 챕터를 자동으로 선택해서 본문을 보여 둔다.
        if CHAPTERS:
            self.chapter_list.SetSelection(0)
            self._show_chapter(0)
        # 포커스를 챕터 목록에 두어 곧바로 위/아래 화살표로 챕터 이동 가능.
        try:
            self.chapter_list.SetFocus()
        except Exception:
            pass
        wx.CallAfter(self._announce_open)

    # ---------- 이벤트 ----------

    def _on_chapter_changed(self, event) -> None:
        idx = event.GetSelection()
        if 0 <= idx < len(CHAPTERS):
            self._show_chapter(idx)

    def _on_chapter_dclick(self, _event=None) -> None:
        # 더블클릭하면 본문 영역으로 포커스 이동 (그래야 화면 낭독을 본문부터 시작).
        try:
            self.text.SetFocus()
            self.text.SetInsertionPoint(0)
        except Exception:
            pass

    def _show_chapter(self, idx: int) -> None:
        title, body = CHAPTERS[idx]
        header = f"[{idx + 1} / {len(CHAPTERS)}] {title}\n" + ("-" * 40) + "\n\n"
        self.text.SetValue(header + body)
        try:
            self.text.SetInsertionPoint(0)
            self.text.ShowPosition(0)
        except Exception:
            pass
        self.body_label.SetLabel(f"내용 - {idx + 1}번 챕터: {title} (&M):")
        try:
            from screen_reader import speak
            speak(f"{idx + 1}번 챕터, {title}.")
        except Exception:
            pass

    def _announce_open(self) -> None:
        try:
            from screen_reader import speak
            speak(
                f"사용 설명서를 열었습니다. 챕터는 모두 {len(CHAPTERS)}개입니다. "
                "위아래 화살표로 챕터를 고르면 오른쪽 칸에 내용이 표시됩니다. "
                "Tab 키로 챕터 목록과 내용 칸을 오갈 수 있습니다. "
                "닫으려면 Esc 키를 누르세요."
            )
        except Exception:
            pass
